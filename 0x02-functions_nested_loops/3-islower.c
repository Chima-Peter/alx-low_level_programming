/**
 * _islower - Write a function that checks for lowercase character.
 *
 * islower - Checks if an interger is uppercase or lowercase.
 *
 * c - This is the interger to be tested.
 *
 * Return: Code returns 1 if lowercase and 0 if uppercase.
 */
int _islower(int c)
{
	return(c >= 'a' && c <= 'z');
}
